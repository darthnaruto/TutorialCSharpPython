"""
Este é um programa exemplo de Python para demonstrar como usar todas as estruturas de LOOPS
possíveis de serem usadas em um programa
"""

#FOR: Simples iteração entre uma coleção . Coleção pode ser de qualquer coisa (inteiro, string)
frutas = ["maçã", "banana", "laranja"]
print("Eu tenho ",len(frutas)," frutas na cesta")
i=1
for fruta in frutas:
    print(i,":",fruta)
    i=i+1

